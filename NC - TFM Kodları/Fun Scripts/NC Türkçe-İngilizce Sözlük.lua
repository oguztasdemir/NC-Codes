function eventPopupAnswer(popupID, player, answer)

    if popupID == 1 then

        answer = lower_tr(answer):gsub(' ','')

        if kelimeler[answer] then

            ui.addTextArea(2, "<p align='center'>Kelimenin anlamı: '<b><J>" .. kelimeler[answer] .. "<N>'</a>", player, 220, 140, 250, 20)

        else

            ui.addTextArea(2, "<p align='center'><b>Bu kelime sözlüğümüzde yoktur</p>", player, 220, 140, 250, 20)

        end

    end

end

 

function lower_tr(str)

    if type(str) ~= 'string' then return '' end

    return str:gsub('\197\158', '\197\159'):gsub('\196\158', '\196\159'):gsub('\196\176', 'i'):gsub('I', '\196\177'):gsub('\195\150', '\195\182'):gsub('\195\156', '\195\188'):gsub('\195\135', '\195\167'):lower()

